public class arrayNumbers23 {
    public static void main(String[] args) {

        // Inisialisasi array langsung
        int[] num = {5, 12, 7, 20};

        System.out.println("Isi elemen array:");
        for (int i = 0; i < 4; i++) {
            System.out.println("Elemen ke-" + i + ": " + num[i]);
        }
    }
}
