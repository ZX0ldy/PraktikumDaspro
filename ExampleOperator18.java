public class ExampleOperator18 {
         public static void main(String[] args) {

        int a= 60;
        int b =20;
        int c = 10;

        System.out.println("***Arithmetic Operator Example***");
        System.out.println("Value of a \t:" + a);
        System.out.println("Value of b\t:"  + b);
        System.out.println("Value of c \t:" + c);
        System.out.println("a + b + c \t:" + (a + b + c));
        System.out.println("a - b - c \t:" + (a - b - c));
        System.out.println("a * c \t\t:" + (a * c));
        System.out.println("a / b \t\t: " + (a / b));
        System.out.println("a & b \t\t: " + (a % b ));
        System.out.println("a + b * c \t: " + (a + b * c));
        System.out.println("(a + b) * c \t: " + ((a + b) * c));
        

    }
}
